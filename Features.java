public class Features {
    private String add;
    private Long edit;
    private Long remove;
    private Long update;
    private Long userId;

    public String getAdd() {
        return add;
    }
    public void setAdd(String add) {
        this.add = add;
    }
    public Long getEdit() {
        return edit;
    }
    public void setEdit(Long edit) {
        this.edit = edit;
    }
    public Long getRemove() {
        return remove;
    }
    public void setRemove(Long remove) {
        this.remove = remove;
    }
    public Long getUpdate() {
        return update;
    }
    public void setUpdate(Long update) {
        this.update = update;
    }
    public Long getUserId() {
        return userId;
    }
    public void setUserId(Long userId) {
        this.userId = userId;
    }
}
